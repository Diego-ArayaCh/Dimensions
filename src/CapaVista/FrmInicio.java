    private void jBtnConfirmarIngresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarIngresoActionPerformed

        if (!Validaciones.ValidacionFrame.jTxt(jTxtNombre.getText(),
                new JDialogNotification(this, true, "Debe revisar el campo de nombre"), jTxtNombre)) {
            return;
        }
        String nombreIngreso = jTxtNombre.getText();
        String contrasenna = ValidacionFrame.obtenerContrasenna(jPswClave.getPassword());
      
        try {
            if (ArchivoUsuarios.getInstance().consultarIngreso(nombreIngreso, contrasenna) == null) {
                contSalida++;
                  if(contSalida==3){
                    JDialogNotification salir = new JDialogNotification(this, true, "Baneo por dudoso ingreso");
                salir.setLocationRelativeTo(JOptionPane.getRootFrame());
                salir.setVisible(true);
                    System.exit(0);
                }else{
                JDialogNotification not = new JDialogNotification(this, true, "Verifique el nombre y la contraseña");
                not.setLocationRelativeTo(JOptionPane.getRootFrame());
                not.setVisible(true);
              
                return;
                  }
            } else {
                PersistenciaUsuario.agregarUsuario(ArchivoUsuarios.getInstance().consultarIngreso(nombreIngreso, contrasenna));
            }
        } catch (Exception ex) {
            JDialogNotification not = new JDialogNotification(this, true, "Hubo un problema comúniquese con el Administrador");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setVisible(true);
            return;
        }

        int cantidadPerfiles = PersistenciaUsuario.getCantidadPerfiles();
       
        ValidacionFrame.perfilesActivos(jBtnP1, jBtnP2, jBtnP3, jBtnP4, jBtnP5, cantidadPerfiles);

        JDialogNotification not = new JDialogNotification(this, true, "Accedió correctamente");
        not.setLocationRelativeTo(JOptionPane.getRootFrame());
        not.setVisible(true);

        jPnlSeleccionPerfil.setVisible(true);
        jPnlIngreso.setVisible(false);


    }//GEN-LAST:event_jBtnConfirmarIngresoActionPerformed

