    private void jBtnConfimarPerfilesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfimarPerfilesActionPerformed
        ArrayList<Perfil> arrayPerfil = new ArrayList();
        String nombrePerfil = "";

        if (ValidacionFrame.perfilVacio(jTxt1, new JDialogNotification(this, true, "Perfil 1 está vacío"))) {
            nombrePerfil = jTxt1.getText();

            arrayPerfil.add(new Perfil(nombrePerfil));

        } else {
            return;
        }

        if (jTxt2.isVisible()) {
            if (ValidacionFrame.perfilVacio(jTxt2, new JDialogNotification(this, true, "Perfil 2 está vacío"))) {
                nombrePerfil = jTxt2.getText();

                arrayPerfil.add(new Perfil(nombrePerfil));

            } else {
                return;
            }
        }
        if (jTxt3.isVisible()) {
            if (ValidacionFrame.perfilVacio(jTxt3, new JDialogNotification(this, true, "Perfil 3 está vacío"))) {
                nombrePerfil = jTxt3.getText();

                arrayPerfil.add(new Perfil(nombrePerfil));

            } else {
                return;
            }
        }
        if (jTxt4.isVisible()) {
            if (ValidacionFrame.perfilVacio(jTxt4, new JDialogNotification(this, true, "Perfil 4 está vacío"))) {
                nombrePerfil = jTxt4.getText();

                arrayPerfil.add(new Perfil(nombrePerfil));

            } else {
                return;
            }

        }
        if (jTxt5.isVisible()) {
            if (ValidacionFrame.perfilVacio(jTxt5, new JDialogNotification(this, true, "Perfil 5 está vacío"))) {
                nombrePerfil = jTxt5.getText();

                arrayPerfil.add(new Perfil(nombrePerfil));

            } else {
                return;
            }
        }
        if (ValidacionFrame.perfilesIguales(arrayPerfil, new JDialogNotification(this, true, "Los nombres deben ser diferentes"))) {
            arrayPerfil.clear();
            return;

        }

        usuario.setArrayPerfil(arrayPerfil);
        try {
            ArchivoUsuarios.getInstance().agregarUsuario(usuario);
        } catch (Exception ex) {
            JDialogNotification not = new JDialogNotification(this, true, "Hubo un problema comúniquese con el Administrador");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setVisible(true);
            return;

        }
        JDialogNotification finalizacion = new JDialogNotification(this, true, "Registro completado");

        finalizacion.setLocationRelativeTo(JOptionPane.getRootFrame());
        finalizacion.setVisible(true);
        jPnlPerfiles.setVisible(false);

    }//GEN-LAST:event_jBtnConfimarPerfilesActionPerformed

