    private void jBtnConfimarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfimarRegistroActionPerformed
    
        if (jCbTipoUsuario.getSelectedIndex() == 0) {
            usuario = new Residente();
            SpinnerNumberModel model = new SpinnerNumberModel(1, 1, 3, 1);
            jSpnCantidadPerfiles.setModel(model);
        }
        if (jCbTipoUsuario.getSelectedIndex() == 1) {
            usuario = new Corporativo();
            SpinnerNumberModel model = new SpinnerNumberModel(1, 1, 5, 1);
            jSpnCantidadPerfiles.setModel(model);
        }

        if (!Validaciones.ValidacionFrame.jTxt(jTxtCorreo.getText(),
                new JDialogNotification(this, true, "Debe revisar el campo de correo"), jTxtCorreo)) {
            return;
        }

        if (!Validaciones.ValidacionFrame.jTxt(jTxtNombreUsuarioRegistro.getText(),
                new JDialogNotification(this, true, "Debe revisar el campo de nombre"), jTxtNombreUsuarioRegistro)) {
            return;
        }

        nombre = jTxtNombreUsuarioRegistro.getText();
        try {
            if (ArchivoUsuarios.getInstance().existente(nombre)) {
                JDialogNotification not = new JDialogNotification(this, true, "Ese nombre ya está en uso");
                not.setLocationRelativeTo(JOptionPane.getRootFrame());
                not.setVisible(true);
                return;

            }
        } catch (Exception ex) {
            JDialogNotification not = new JDialogNotification(this, true, "Hubo un problema comúniquese con el Administrador");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setVisible(true);
            return;
        }
        if (!Validaciones.ValidacionFrame.correoElectronico(jTxtCorreo.getText(), new JDialogNotification(this, true, "El correo no es válido"))) {
            return;
        }

        correo = jTxtCorreo.getText();

        if (!Validaciones.ValidacionFrame.contrasenna(jPswClaveRegistro.getText(), new JDialogNotification(this, true, "ingrese una contraseña válida"))) {
            return;
        }

        contrasenna = ValidacionFrame.obtenerContrasenna(jPswClaveRegistro.getPassword());
       
        usuario.setClave(contrasenna);
        usuario.setCorreo(correo);
        usuario.setNombre(nombre);
        jPnlRegistro.setVisible(false);
        jPnlTarjeta.setVisible(true);
        jPnlPerfiles.setVisible(false);

    }//GEN-LAST:event_jBtnConfimarRegistroActionPerformed

    private void jBtnConfimarTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfimarTarjetaActionPerformed
        tarjeta = new Tarjeta();
        if (!Validaciones.ValidacionFrame.jTxt(jTxtNumeroTarjeta.getText(),
                new JDialogNotification(this, true, "Ingrese el número de tarjeta"), jTxtNumeroTarjeta)) {
            return;
        }

        if (!ValidacionFrame.soloEnteros(jTxtNumeroTarjeta.getText(), new JDialogNotification(this, true, "El número de tarjeta solo debe contener números"))) {

            return;
        }

        numeroTarjeta = jTxtNumeroTarjeta.getText();

        if (!Validaciones.ValidacionFrame.jRdb(new JDialogNotification(this, true, "Debe seleccionar el tipo de tarjeta"),
                jRdbVisa, jRdbMastercard)) {
            return;
        }
        if (jRdbMastercard.isSelected()) {
            tipoTarjeta = TipoTarjeta.MASTER_CARD;
        } else {
            tipoTarjeta = TipoTarjeta.VISA;
        }

        mesVencimiento = (int) jSpnMesVencimiento.getValue();
        annoVencimiento = jSldAnnoVencimiento.getValue();
        tarjeta.setAnnoVencimiento(annoVencimiento);
        tarjeta.setMesVencimiento(mesVencimiento);
        tarjeta.setNumeroTarjeta(numeroTarjeta);
        tarjeta.setTipoTarjeta(tipoTarjeta);

        if (!tarjeta.validarFechaTarjeta()) {
            JDialogNotification not = new JDialogNotification(this, true, "Error en la fecha de su tarjeta");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setVisible(true);
            return;
        }
        if (!tarjeta.validarNumeroTarjeta()) {
            JDialogNotification not = new JDialogNotification(this, true, "Error en el número de su tarjeta");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setVisible(true);
            return;

        }
        if (!tarjeta.validarTipoTarjeta()) {
            JDialogNotification not = new JDialogNotification(this, true, "Error, el tipo de tarjeta no coincide con el número");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setVisible(true);
            return;
        }

        usuario.setTarjeta(tarjeta);
        jPnlPerfiles.setVisible(true);
        jPnlTarjeta.setVisible(false);
        jPnlRegistro.setVisible(false);
        jPnlIngreso.setVisible(false);
    }//GEN-LAST:event_jBtnConfimarTarjetaActionPerformed

