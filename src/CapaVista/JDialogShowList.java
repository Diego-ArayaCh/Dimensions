    public String factura(){
        String cadena="";
        
        for (Descarga descarga : listaDescargas) {
            cadena+="\n"+descarga.getContenidoDescargado().toString()+"\n";
        }
        
      
//      NumberToLetterConverter letras=new NumberToLetterConverter();
//           String montoDolaresLetras=letras.Convertir(montoDolares+"", true);
//           
//         String montoColonesLetras=  letras.Convertir(montoDolares*Moneda.Colones.getCambio()+"", true);


int parteEntera$=(int)montoDolares;
double decimal$=(montoDolares-parteEntera$)*100;

int parteEnteraC=(int)(montoDolares*Moneda.Colones.getCambio());
double decimalC=((montoDolares*Moneda.Colones.getCambio())-parteEnteraC)*100;

        cadena=cadena+"\nMonto Dolares "+Validaciones.ValidacionFrame.precioEnLetras(parteEntera$)+ " Con "+Validaciones.ValidacionFrame.precioEnLetras((int)decimal$);
         cadena=cadena+"\nMonto Colones "+Validaciones.ValidacionFrame.precioEnLetras(parteEnteraC)+" Con "+Validaciones.ValidacionFrame.precioEnLetras((int)decimalC);
         return cadena;
    }
