    public void actualizarPrecios() {
        montoDolares = 0;
        for (Descarga descarga : listaDescargas) {
            montoDolares += descarga.getContenidoDescargado().costoTotal();
            
        }
        double montoColones = montoDolares * Moneda.Colones.getCambio();
        jLblColones.setText("Total en Colones "+montoColones);
        jLblDolares.setText("Total en Dolares "+montoDolares);
    }

