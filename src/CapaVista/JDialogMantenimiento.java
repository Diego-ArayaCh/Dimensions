    private void jBtnAnnadirIdiomaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAnnadirIdiomaActionPerformed
        boolean estaRepetido = false;

        Idioma idioma = (Idioma) jCboIdioma.getSelectedItem();

        for (Idioma idi : listaIdiomas) {
            if (idi.equals(idioma)) {
                estaRepetido = true;

            }
        }

        if (estaRepetido) {
            JDialogNotification not = new JDialogNotification(nexo, true, "El idioma está en la lista");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setBackground(colorPressed);
            not.setVisible(true);
            return;
        } else {

            listaIdiomas.add((Idioma) jCboIdioma.getSelectedItem());

            actualizarLista();
        }


    }//GEN-LAST:event_jBtnAnnadirIdiomaActionPerformed
    public void actualizarLista() {
        modeloLista.clear();
        for (Idioma idioma : listaIdiomas) {
            modeloLista.addElement(idioma);
        }

    }

