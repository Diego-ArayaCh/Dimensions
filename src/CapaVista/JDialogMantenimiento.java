    private void jBtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAceptarActionPerformed
        String codigo;
        AudioVisual contenido = null;
        if (!jTxtCodigo.equals("")) {
            codigo = jTxtCodigo.getText();

        } else {
            JDialogNotification not = new JDialogNotification(nexo, true, "Ingrese el código");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setBackground(colorPressed);
            not.setVisible(true);
            jTxtCodigo.requestFocus();
            return;
        }
        String titulo;
        if (!jTxtTitulo.getText().equals("")) {
            titulo = jTxtTitulo.getText();

        } else {
            JDialogNotification not = new JDialogNotification(nexo, true, "Ingrese el título");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setBackground(colorPressed);
            not.setVisible(true);
            jTxtTitulo.requestFocus();
            return;
        }
        if (listaIdiomas.isEmpty()) {
            JDialogNotification not = new JDialogNotification(nexo, true, "Seleccione los idiomas disponibles");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setBackground(colorPressed);
            not.setVisible(true);
            jCboIdioma.requestFocus();

        } else {

        }
        LocalDate fecha;
        try {
            fecha = LocalDate.parse(jTxtFecha.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            if (fecha.isAfter(LocalDate.now())) {
                JDialogNotification not = new JDialogNotification(nexo, true, "Ingrese una fecha válida");
                not.setLocationRelativeTo(JOptionPane.getRootFrame());
                not.setBackground(colorPressed);
                not.setVisible(true);
                jTxtFecha.requestFocus();
                jTxtFecha.selectAll();
                return;
            }
        } catch (Exception e) {
            JDialogNotification not = new JDialogNotification(nexo, true, "Ingrese la fecha");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());
            not.setBackground(colorPressed);
            not.setVisible(true);
            jTxtFecha.requestFocus();
            return;
        }
        boolean isCompleta;
        boolean isOscar;
        String director;
        if (jRdbSerie.isSelected()) {
            if (listaTemporada.isEmpty()) {
                JDialogNotification not = new JDialogNotification(nexo, true, "Debe agregar al menos una temporada");
                not.setLocationRelativeTo(JOptionPane.getRootFrame());
                not.setBackground(colorPressed);
                not.setVisible(true);
                jFmtCapitulos.requestFocus();
                return;

            } else {
                isCompleta = jCkIsCompleta.isSelected();
            }
            Genero genero = (Genero) jCboGeneros.getSelectedItem();
            contenido = new Serie(codigo, titulo, fecha, listaIdiomas, calificacion, genero, jCboIsOriginal.isSelected());
            ((Serie) contenido).setIsCompleta(isCompleta);
            ((Serie) contenido).setTemporadas(listaTemporada);
            contenido.setCantidadDescargas(cantidadDescargas);
        }

        if (jRdbPelicula.isSelected()) {

            isOscar = jCkIsCompleta.isSelected();

//            if(jTxtDirector.getText().equals("")){
//                JDialogNotification not = new JDialogNotification(nexo, true, "Ingrese el director de la película");
//            not.setLocationRelativeTo(JOptionPane.getRootFrame());
//          
//            not.setVisible(true);
//            jTxtDirector.requestFocus();
//                return;
//            }else{
            director = jCboDirector.getSelectedItem() + "";

//            }
            Genero genero = (Genero) jCboGeneros.getSelectedItem();
            System.out.println(calificacion);
            contenido = new Pelicula(codigo, titulo, fecha, listaIdiomas, calificacion, genero, jCboIsOriginal.isSelected());
            ((Pelicula) contenido).setIsOscar(isOscar);
            ((Pelicula) contenido).setDirector(director);
            contenido.setCantidadDescargas(cantidadDescargas);
        }
        if (contenido == null) {
            JDialogNotification not = new JDialogNotification(nexo, true, "Seleccione el tipo de contenido");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());

            not.setVisible(true);

        }

        try {
            if (ArchivoContenidoAudioVisual.getInstance().consultarAudioVisual(codigo) == null) {
                ArchivoContenidoAudioVisual.getInstance().agregarAudioVisual(contenido);
            } else {
                if (!modificar) {
                    JDialogNotification not = new JDialogNotification(nexo, true, "El contenido ya existe ");
                    not.setLocationRelativeTo(JOptionPane.getRootFrame());

                    not.setVisible(true);
                    return;
                } else {

                    ArchivoContenidoAudioVisual.getInstance().modificarAudioVisual(contenido);

                }
            }

        } catch (Exception ex) {
            JDialogNotification not = new JDialogNotification(nexo, true, "Hubo un problema comuníquese con el administrador");
            not.setLocationRelativeTo(JOptionPane.getRootFrame());

            not.setVisible(true);
            return;
        }

        modeloLista.clear();
        jBtnCancelar.doClick();
        nexo.ActualizarTablaMantenimiento();
    }//GEN-LAST:event_jBtnAceptarActionPerformed

